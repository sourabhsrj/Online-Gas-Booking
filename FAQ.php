            <b>1)What is meant by Preferred Time Delivery? How is it different from normal home delivery?</b><br>
        Normally LPG cylinders are delivered based on your booking in serial order on first come first serve basis. No separate provision for refill delivery as per your convenient day and time is provided. Under Preferred Day and Time Delivery, you can now choose the service to receive the cylinder at home on the day and time of your choice at a cost. The day and time slots fixed for the delivery and the charges for the service are given below:
Time Slot	Charges per delivery in Towns with population of ten lakh and above	Charges per delivery in other Towns
Before 8 AM	Rs.50	Rs.40
8AM to 11 AM	Rs.25	Rs.20
11 AM to 3 PM	Rs.25	Rs.20
3 PM to 6 PM	Rs.25	Rs.20
6 PM to 8 PM	Rs.50	Rs.40
Only Saturday/Sunday (8 AM to 6 PM)	Rs.25	Rs.20</p>
        <p>
            <b> 2) What are the options available for me as a preferred time customer?</b><br>
You can make your choice from any of the following three options:
Preferred Time Customer: You can prefer a time on any day starting from Monday upto Friday. Such a choice indicate that you are ready to accept the cylinder on any day from Monday to Friday on the fixed time slot specified above. If you miss the delivery , distributor will deliver on the next day same time.
Preferred Day and Time Customer: You can also make a choice of selecting a day in addition to time. Which means you are is ready to accept only on the specified day and time. Even if you miss the day and time next delivery will be next week same day and time.
Saturday/Sunday Preferred Customer:You can prefer either Saturday or Sunday any time indicating that you are ready to accept the cylinder either on Saturday or Sunday as per the preference any time from 8AM to 6PM.</p>
        <p><b>3) Do I get a cylinder on my immediate next preferred day and time of my booking?</b><br>
You may undertake simple checks at the time of your refill receipt :- LPG cylinders are subsidized and hence are delivered on first come first served basis. Hence you will not receive the cylinder out of turn. After your call matures, the supply will be made to you on next preferred day and time. For example if you are a preferred time customer, you will receive the cylinder next day on the preferred time slot after your call matures. For example if you are a 8AM to 11 AM preferred time customer and have booked your refill on Saturday 1st of June. But on that day distributor was delivering cylinders booked by customers on 25th day of May. After your call matures, say on Wednesday 5th June, you will receive the cylinder on same Wednesday between 8 AM and 11 PM. However if you are a preferred day and time customer of Monday and your booking matures on Wednesday 5th June, you will receive the cylinder only on 10th June, Monday.</p>
        <p><b>4) Is this facility available with all distributors in India?</b><br>
The facility is now available with all distributors of Indianoil Corporation Limited(Indane) customers in Mumbai, Kolkata, Delhi, Chennai, Bangluru, Hyderabad and Pune and also outskirts of these cities i.e. Gurgaon, Noida, Faridabad, Ghaziabad, Sonepat, Thane, Navi Mumbai, Kalyan, Mira Bhayander etc. The scheme may be extended to all towns having population of about 10 lakhs shortly.<p>
