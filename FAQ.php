        <p><b>6) Do I need to mention my preferred day and time every time I book for a refill?</b><br>
Once you register as a preferred time customer for a specific day and time you need not mention the day and time at the time of refill booking. The system provided with the distributors will automatically identify your status.</p>
        <p><b>7) Do I have an option to avail cylinder on any day-any time after registration?</b><br>
No. Once you are a Preferred Time Customer, you will receive cylinders only on the time/day /day and time you have already chosen.
<p><b>8) Can I withdraw myself from availing this facility after registration?</b><br>
You can de-register through submission of an application physically to distributor or through web site.</p>
   <p> <b>9) What is the validity period of my registration ?</b><br>
Registration is one time. But you can change your day and time of choice after a period of six months. When you change or register, the acceptance of same in the system may take a maximum of 48 hours.</p>
   <p><b>10) What are my obligations as a customer when I register as Preferred Time Customer?</b><br>
Once you register and become a preferred time customer you or your representative shall be available at home for receiving the cylinder delivered on the day and time chosen by you (Say Sunday 9th June). You will not be delivered with the cylinder on any other day and time. In case you are not available on the preferred day and time(Sunday, 9th June), distributor will deliver it only on the next week same day and time(Sunday,16th June). If on the second time you fail to receive, your status of preferred time customer will be withdrawn for next six months and your refill booking will be cancelled. Discretion to restart the facility within six months will lie with the distributor for which you need to contact him/her.
   <p><b>11) Is the delivery possible on Preferred Date?</b><br>
This facility is only for preferred day and no specific booking is possible as of now for preferred Date.
   <p><b>12) In case I am available at home and distributor fails to deliver. When do I get the cylinder?</b><br>
In case the distributor fails to deliver due to reasons solely attributable to him, he will call you and deliver it to you subsequently on any day by giving a rebate of Rs 20.
   <p><b>13) Can I register my option for Preferred time through IVRS/SMS?</b><br>
No. Presently you cannot opt for preferred time through IVRS/SMS. However in case you are registered as a preferred time customer even if you book you refill through IVRS/SMS you will receive delivery on the day and time chosen by you.
